package oo.abstraction;

public class Shoes {
    int id;
	String brand;
    int size;
    String color;
    String pattern;
    String use;
    int price;
      
    public Shoes(String brand, int size,String color,String pattern,String use,int price){
      this.brand=brand;
      this.size=size;
      this.color=color;
      this.pattern=pattern;
      this.use=use;
      this.price=price;
    }
    
}